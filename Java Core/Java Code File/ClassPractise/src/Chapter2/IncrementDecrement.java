package Chapter2;

public class IncrementDecrement {

    public static void main(String[] args) {
        double x = 1.0;
        double y = 1.0;
        double z = 1.0;

        System.out.println("x: " + x);
        System.out.println("x++: " + x++);
        System.out.println("++x: " + ++x);
        System.out.println("y: " + y);
        System.out.println("y++: " + y++);
        System.out.println("++y: " + ++y);
        System.out.println("z: " + z);
        System.out.println("z++: " + z++);
        System.out.println("++z: " + ++z);
        System.out.println("x: " + x);
        System.out.println("x--: " + x--);
        System.out.println("--x: " + --x);
        System.out.println("y: " + y);
        System.out.println("y--: " + y--);
        System.out.println("--y: " + --y);
        System.out.println("z: " + z);
        System.out.println("z--: " + z--);
        System.out.println("--z: " + --z);

        int i = 5;
        int k = ++i + i;
        System.out.println("K: " + k);

    }
}





























