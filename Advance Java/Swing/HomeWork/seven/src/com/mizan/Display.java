    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String name = jTextField1.getText().trim(); 
       String age = jTextField2.getText().trim(); 
       String contact = jTextField3.getText().trim(); 
       String gender = null; 
       if(jRadioButton1.isSelected()){
       gender=jRadioButton1.getText().trim();
       }
       if(jRadioButton2.isSelected()){
       gender=jRadioButton2.getText().trim();
       }
       StringBuilder course= new StringBuilder(); 
       if(jCheckBox1.isSelected()){
       course.append(jCheckBox1.getText().trim());
       }
       if(jCheckBox2.isSelected()){
       course.append(jCheckBox2.getText().trim());
       }
       if(jCheckBox3.isSelected()){
       course.append(jCheckBox3.getText().trim());
       }
       String remark= jTextArea1.getText().trim(); 
       String location= jComboBox1.getSelectedItem().toString(); 
       String courses= course.toString(); 
       
       Student student = new Student(name, age, contact, gender, courses, remark, location); 
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel(); 
       Object row[]= new Object[7]; 
       row[0]=student.getName();
       row[1]=student.getAge();
       row[2]=student.getContact();
       row[3]=student.getGender();
       row[4]=student.getCourse();
       row[5]=student.getRemark();
       row[6]=student.getLocation();
       model.addRow(row);
       Utils.writeTofile("mizan", student);
    }//GEN-LAST:event_jButton1ActionPerformed

