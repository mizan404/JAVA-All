    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        String name = jName.getText().trim();
        String contact = jContact.getText().trim();
        String email = jEmail.getText().trim();
        String gender = null;
        if (rMale.isSelected()) {
            gender = rMale.getText().trim();
        }
        if (rFemale.isSelected()) {
            gender = rFemale.getText().trim();
        }
        StringBuilder course = new StringBuilder();
        if (chkJ2ee.isSelected()) {
            course.append(chkJ2ee.getText().trim() + " ");
        }
        if (chkPhp.isSelected()) {
            course.append(chkPhp.getText().trim() + " ");
        }
        if (chkGave.isSelected()) {
            course.append(chkGave.getText().trim());
        }
        String remark = txtArea.getText().trim();
        String location = cmbLocation.getSelectedItem().toString();
        String courses = course.toString();
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Email must be filled!!");
        } else if (email.length() < 8) {
            JOptionPane.showMessageDialog(rootPane, "Email must be 8 characters!!");
        } else if (!validEmail(email)) {
            JOptionPane.showMessageDialog(rootPane, "Email must be Valid!!");
        } else {
            Student student = new Student(name, contact, email, gender, courses, remark, location);
            DefaultTableModel model = (DefaultTableModel) jTable.getModel();
            Object row[] = new Object[7];
            row[0] = student.getName();
            row[1] = student.getContact();
            row[2] = student.getEmail();
            row[3] = student.getGender();
            row[4] = student.getCourse();
            row[5] = student.getRemark();
            row[6] = student.getLocation();
            model.addRow(row);
            utils.writeTofile("mizan", student);
            clear();
    }//GEN-LAST:event_btnSubmitActionPerformed
    }
