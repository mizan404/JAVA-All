    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        String name = txtName.getText().trim();      // TODO add your handling code here:
        String email = txtEmail.getText().trim();
        String gender = null;
        if (rMale.isSelected()) {
            gender = rMale.getText().trim();
        }
        if (rFemale.isSelected()) {
            gender = rFemale.getText().trim();
        }
        StringBuilder hobby = new StringBuilder();
        if (chkReading.isSelected()) {
            hobby.append(chkReading.getText().trim() + " ");
        }
        if (chkEating.isSelected()) {
            hobby.append(chkEating.getText().trim() + " ");
        }
        if (chkSleeping.isSelected()) {
            hobby.append(chkSleeping.getText().trim());

            String round = cmbRound.getSelectedItem().toString();

            /////////////
            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Enter Your Name");
            } else if (name.length() < 3) {
                JOptionPane.showMessageDialog(rootPane, "Enter at least 3 Letter");
            } else if (email.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Enter a Email");

            } else if (!isValidEmail(email)) {
                JOptionPane.showMessageDialog(rootPane, "Enter a Valid Email");

            } else if (gender == null) {
                JOptionPane.showMessageDialog(rootPane, "Select Your Gender");

            } else if (hobby.toString().length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Select Your Hobby");

            } else if (round.equalsIgnoreCase("Select A Round ")) {
                JOptionPane.showMessageDialog(rootPane, "Select Your Round");
            } else {
                Info1 stu = new Info1(serial++, name, email, gender, hobby.toString(), round);
                DefaultTableModel model = (DefaultTableModel) TblDisplay.getModel();
                Object[] row = new Object[6];
                row[0] = stu.getSerial();
                row[1] = stu.getName();
                row[2] = stu.getEmail();
                row[3] = stu.getGender();
                row[4] = stu.getHobby();
                row[5] = stu.getRound();
                model.addRow(row);
                Utils.writeTofile("itsbeing", stu);
                clear();
            }

        }
// TODO add your handling code here:
    }//GEN-LAST:event_btnSubmitActionPerformed

