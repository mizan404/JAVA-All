    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String gender=null;
        if(rMale.isSelected()){
            gender=rMale.getText().trim();
        }
        if(rFemale.isSelected()){
            gender=rFemale.getText().trim();
        }
        Student student=new Student(txtName.getText().trim(), txtEmail.getText().trim(), gender, cmbRound.getSelectedItem().toString(), txtNote.getText().trim());
        con.save(student);
        displayDataIntoTable();
        JOptionPane.showMessageDialog(null, "Successfully Added");
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String gender=null;
        if(rMale.isSelected()){
            gender=rMale.getText().trim();
        }
        if(rFemale.isSelected()){
            gender=rFemale.getText().trim();
        }
        Student student=new Student(Integer.parseInt(txtId.getText().trim()),txtName.getText().trim(), txtEmail.getText().trim(), gender, cmbRound.getSelectedItem().toString(), txtNote.getText().trim());
        con.update(student);
        displayDataIntoTable();
        JOptionPane.showMessageDialog(null, "Successfully Updated");
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       
        con.delete(Integer.parseInt(txtId.getText().trim()));
        displayDataIntoTable();
        JOptionPane.showMessageDialog(null, "Successfully Deleted");
    }//GEN-LAST:event_btnDeleteActionPerformed

