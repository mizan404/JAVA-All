    private void jCalAgeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCalAgeActionPerformed
        String dateofbirth = ((JTextField) jDateChooser.getDateEditor().getUiComponent()).getText();
//        System.out.println(" "+dateofbirth);
        String dob[] = dateofbirth.split("/");
//        System.out.println(""+ dob[0]);
//        System.out.println(""+ dob[1]);
//        System.out.println(""+ dob[2]);
        int day = Integer.parseInt(dob[0]);
        int month = Integer.parseInt(dob[1]);
        int year = Integer.parseInt(dob[2]);

        LocalDate seletedDate = LocalDate.of(year, month, day);
        LocalDate currentDate = LocalDate.now();

        int resultYear = Period.between(seletedDate, currentDate).getYears();
        int resultMonth = Period.between(seletedDate, currentDate).getMonths();
        int resultDay = Period.between(seletedDate, currentDate).getDays();
        jViewYear.setText("" + resultYear);
        jViewMonth.setText("" + resultMonth);
        jViewDay.setText("" + resultDay);


    }//GEN-LAST:event_jCalAgeActionPerformed

