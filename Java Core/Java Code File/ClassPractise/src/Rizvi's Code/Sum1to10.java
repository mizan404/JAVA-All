
package evidence;

import java.util.Scanner;

public class Sum1to10 {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
         int count=sc.nextInt();
        int sum=0;
        for(int i=sum;i<=count;i++){
            sum+=i;
        }
        System.out.println(sum);
    }
}
