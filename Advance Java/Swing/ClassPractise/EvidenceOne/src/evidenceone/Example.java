    private void TblDisplayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblDisplayMouseClicked
        // TODO add your handling code here:
        int i = TblDisplay.getSelectedRow();
        TableModel model = TblDisplay.getModel();
        txtName.setText(model.getValueAt(i, 1).toString());
        txtEmail.setText(model.getValueAt(i, 2).toString());
        String gender = model.getValueAt(i, 3).toString();
        if (gender.equalsIgnoreCase("Male")) {
            rMale.setSelected(true);
        }
        if (gender.equalsIgnoreCase("Female")) {
            rFemale.setSelected(true);
        }
        String hobby = model.getValueAt(i, 4).toString();
        String hobb[] = hobby.split(" ");
        if (hobb.length == 1) {
            clearHobby();
            if (hobb[0].equalsIgnoreCase("Reading")) {
                chkReading.setSelected(true);
            } else if (hobb[0].equalsIgnoreCase("Eating")) {
                chkEating.setSelected(true);
            } else if (hobb[0].equalsIgnoreCase("Sleeping")) {
                chkSleeping.setSelected(true);
            }

        }
        if (hobb.length == 2) {
            clearHobby();
            if (hobb[0].equalsIgnoreCase("Reading")) {
                chkReading.setSelected(true);
            } else if (hobb[0].equalsIgnoreCase("Eating")) {
                chkEating.setSelected(true);
            } else if (hobb[0].equalsIgnoreCase("Sleeping")) {
                chkSleeping.setSelected(true);
            }
            if (hobb[1].equalsIgnoreCase("Reading")) {
                chkReading.setSelected(true);
            } else if (hobb[1].equalsIgnoreCase("Eating")) {
                chkEating.setSelected(true);
            } else if (hobb[1].equalsIgnoreCase("Sleeping")) {
                chkSleeping.setSelected(true);
            }

        } else {
            clearHobby();
            if (!hobb[0].isEmpty() && hobb[0].equalsIgnoreCase("Reading")) {
                chkReading.setSelected(true);
            }
            if (!hobb[1].isEmpty() && hobb[1].equalsIgnoreCase("Eating")) {
                chkEating.setSelected(true);
            }
            if (!hobb[2].isEmpty() && hobb[2].equalsIgnoreCase("Sleeping")) {
                chkSleeping.setSelected(true);
            }

        }
        String round = model.getValueAt(i, 5).toString();
        cmbRound.setSelectedItem(round);

    }//GEN-LAST:event_TblDisplayMouseClicked

